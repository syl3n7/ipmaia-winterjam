// Helper function to get the most recent game jam
export function getCurrentGameJam() {
  // Currently hardcoded - can be made dynamic later
  return gameJams["2025"]["winter"];
}

// Helper function to get a specific game jam
export function getGameJam(year, season) {
  if (!gameJams[year] || !gameJams[year][season]) {
    return null;
  }
  return gameJams[year][season];
}

// Helper function to get all game jams for display in navbar or elsewhere
export function getAllGameJams() {
  const jams = [];
  
  Object.keys(gameJams).forEach(year => {
    Object.keys(gameJams[year]).forEach(season => {
      const jam = gameJams[year][season];
      jams.push({
        id: jam.id,
        name: jam.name,
        path: `/archive/${year}/${season}`,
        isCurrent: year === "2025" && season === "winter" // Update this logic as needed
      });
    });
  });
  
  return jams;
}